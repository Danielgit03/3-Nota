

public class iterativas {
    public static void main(String[] args) {
        //While (Mientras)
        for (int i = 0; i < 5; i++) {
            System.out.println("i es " + i);
        }


        int i = 0;
        do {
            System.out.println("i es " + i);
            i++;
        } while (i < 5);











    }


}
